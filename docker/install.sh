# Function to build and start containers
build_and_start() {
    print_status "Building Docker image..."
    
    # Build the image
    docker build -t uptime-monitor-pro:latest .
    
    print_status "‚úÖ Docker image built successfully"
    
    print_status "Starting containers..."
    
    # Start the services
    $COMPOSE_CMD up -d
    
    print_status "‚úÖ Containers started successfully"
}

# Function to wait for service to be ready
wait_for_service() {
    print_status "Waiting for service to be ready..."
    
    local max_attempts=30
    local attempt=1
    
    while [ $attempt -le $max_attempts ]; do
        if curl -s http://localhost:3000/api/health >/dev/null 2>&1; then
            print_status "‚úÖ Service is ready!"
            return 0
        fi
        
        print_status "Attempt $attempt/$max_attempts - waiting for service..."
        sleep 2
        ((attempt++))
    done
    
    print_warning "Service may still be starting up..."
    return 1
}

# Function to show container status
